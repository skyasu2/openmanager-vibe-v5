interface SlackNotification {
  id: string;
  message: string;
  severity: 'low' | 'medium' | 'high' | 'critical';
  timestamp: Date;
  status: 'sending' | 'sent' | 'failed';
  slackMessageId?: string;
}

// Ìñ•ÏÉÅÎêú fetch Ìï®Ïàò (ÌÉÄÏûÑÏïÑÏõÉ Î∞è ÏóêÎü¨ Ï≤òÎ¶¨)
const fetchWithTimeout = async (
  url: string,
  options: RequestInit = {},
  timeout = 15000
) => {
  const controller = new AbortController();
  const timeoutId = setTimeout(() => controller.abort(), timeout);

  try {
    const response = await fetch(url, {
      ...options,
      signal: controller.signal,
      headers: {
        'Content-Type': 'application/json',
        ...options.headers,
      },
    });

    clearTimeout(timeoutId);

    if (!response.ok) {
      throw new Error(`HTTP ${response.status}: ${response.statusText}`);
    }

    return await response.json();
  } catch (error) {
    clearTimeout(timeoutId);

    if (error.name === 'AbortError') {
      throw new Error(`ÏöîÏ≤≠ ÌÉÄÏûÑÏïÑÏõÉ (${timeout}ms Ï¥àÍ≥º)`);
    }

    throw error;
  }
};

// Îã®Ïùº Ï±ÑÎÑê Ïä¨Îûô ÏïåÎ¶º ÌõÖ
export const useSlackNotifications = () => {
  const [notifications, setNotifications] = useState<SlackNotification[]>([]);

  const sendSlackNotification = async (data: {
    message: string;
    severity: 'low' | 'medium' | 'high' | 'critical';
  }) => {
    const notification: SlackNotification = {
      id: crypto.randomUUID(),
      message: data.message,
      severity: data.severity,
      timestamp: new Date(),
      status: 'sending',
    };

    // Ï¶âÏãú UIÏóê ÌëúÏãú
    setNotifications(prev => [notification, ...prev].slice(0, 10)); // ÏµúÎåÄ 10Í∞ú

    try {
      // Í∞úÏÑ†Îêú API Ìò∏Ï∂ú
      const result = await fetchWithTimeout(
        '/api/slack/send',
        {
          method: 'POST',
          body: JSON.stringify({
            message: data.message,
            severity: data.severity,
          }),
        },
        15000
      ); // 15Ï¥à ÌÉÄÏûÑÏïÑÏõÉ

      if (result.success) {
        // ÏÑ±Í≥µ ÏÉÅÌÉúÎ°ú ÏóÖÎç∞Ïù¥Ìä∏
        setNotifications(prev =>
          prev.map(n =>
            n.id === notification.id
              ? { ...n, status: 'sent', slackMessageId: result.messageId }
              : n
          )
        );
      } else {
        throw new Error(result.error || 'Ï†ÑÏÜ° Ïã§Ìå®');
      }
    } catch (error) {
      console.error('Ïä¨Îûô ÏïåÎ¶º Ï†ÑÏÜ° Ïã§Ìå®:', error);

      // Ïã§Ìå® ÏÉÅÌÉúÎ°ú ÏóÖÎç∞Ïù¥Ìä∏
      setNotifications(prev =>
        prev.map(n =>
          n.id === notification.id ? { ...n, status: 'failed' } : n
        )
      );
    }
  };

  const removeNotification = (id: string) => {
    setNotifications(prev => prev.filter(n => n.id !== id));
  };

  return { notifications, sendSlackNotification, removeNotification };
};

